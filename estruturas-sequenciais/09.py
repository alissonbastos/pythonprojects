'''
Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
'''

lado = (int(input('Digite o valor da lateral do quadrado: ')))
area = lado ** 2
dobro = area * 2
print(f'O valor da área do quadrado é equivalente a: {area}, e o dobro do seu tamanho é {dobro}')